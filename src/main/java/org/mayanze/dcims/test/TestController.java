    /**
     * reciveMsg
     */
    @RequestMapping("/reciveMsg")
    public void reciveMsg(HttpServletRequest request) throws IOException {
        String ipAddress = getIpAddress(request);
        if(StringUtils.isEmpty(ipAddress)){
            ipAddress = getClientIpAddress(request);
        }
        if(StringUtils.isEmpty(ipAddress)){
            ipAddress = getClientIpAddr(request);
        }
        if(StringUtils.isEmpty(ipAddress)){
            ipAddress = getIpAddr(request);
        }
        String lines = getLines(request);
        log.warn("ip:" + ipAddress + ";进出围栏推送消息:" + lines);
    }

